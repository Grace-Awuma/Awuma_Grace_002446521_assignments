// Validation method
private boolean validateFields() {
    if (isFieldEmpty(fieldProfileName.getText())) {
        JOptionPane.showMessageDialog(this, "Name is required!", "Validation Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    if (isFieldEmpty(fieldProfileAge.getText())) {
        JOptionPane.showMessageDialog(this, "Age is required!", "Validation Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    if (isFieldEmpty(fieldCountry.getText())) {
        JOptionPane.showMessageDialog(this, "Country is required!", "Validation Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    if (isFieldEmpty(fieldPhoneNum.getText())) {
        JOptionPane.showMessageDialog(this, "Phone Number is required!", "Validation Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    if (isFieldEmpty(fieldEmail.getText())) {
        JOptionPane.showMessageDialog(this, "Email is required!", "Validation Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    return true; // all fields valid
}
