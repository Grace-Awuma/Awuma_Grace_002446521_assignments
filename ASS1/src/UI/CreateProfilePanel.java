    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        // Validate before saving
    if (!validateFields()) {
        return; // stop if any field is invalid
    }



  // Collect values from text fields
    String name = fieldProfileName.getText();
    String age = fieldProfileAge.getText();
    String country = fieldCountry.getText();
    String phone = fieldPhoneNum.getText();
    String email = fieldEmail.getText();

    // Save into person object
    person.setName(name);
    person.setAge(age);
    person.setCountry(country);
    person.setPhone(phone);
    person.setEmail(email);

    // Show success pop-up
    JOptionPane.showMessageDialog(this, "Profile saved successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);

    // Clear all fields after saving
    fieldProfileName.setText("");
    fieldProfileAge.setText("");
    fieldCountry.setText("");
    fieldPhoneNum.setText("");
    fieldEmail.setText("");

    }//GEN-LAST:event_btnSaveActionPerformed


