// Helper method to check if field is empty
private boolean isFieldEmpty(String text) {
    return text == null || text.trim().isEmpty();
}

// Validation method
private boolean validateLocalAddressFields() {
    if (isFieldEmpty(fieldLocalAddressName.getText())) {
        JOptionPane.showMessageDialog(this, "Name is required!", "Validation Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    if (isFieldEmpty(fieldLocalAdresStrt.getText())) {
        JOptionPane.showMessageDialog(this, "Street is required!", "Validation Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    if (isFieldEmpty(fieldLocalAddressCity.getText())) {
        JOptionPane.showMessageDialog(this, "City is required!", "Validation Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    if (isFieldEmpty(fieldLocalAddressState.getText())) {
        JOptionPane.showMessageDialog(this, "State is required!", "Validation Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    if (isFieldEmpty(fieldLocalAddressCountry.getText())) {
        JOptionPane.showMessageDialog(this, "Country is required!", "Validation Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    return true; // all fields valid
}

    
    
    private void btnLocalAdresSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLocalAdresSaveActionPerformed
        // TODO add your handling code here:
        
    // Validate before saving
    if (!validateLocalAddressFields()) {
        return; // stop if validation fails
    }

    // Collect values from text fields
    String name = fieldLocalAddressName.getText();
    String street = fieldLocalAdresStrt.getText();
    String city = fieldLocalAddressCity.getText();
    String state = fieldLocalAddressState.getText();
    String country = fieldLocalAddressCountry.getText();

    // Save into localAddress object
    person.setName(name);
    if (person.getLocalAddress() == null) {
        person.setLocalAddress(new Address());
    }
    person.getLocalAddress().setStreet(street);
    person.getLocalAddress().setCity(city);
    person.getLocalAddress().setState(state);
    person.getLocalAddress().setCountry(country);

    // Show success message
    JOptionPane.showMessageDialog(this, "Local Address saved successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);

    // Clear all fields after saving
    fieldLocalAddressName.setText("");
    fieldLocalAdresStrt.setText("");
    fieldLocalAddressCity.setText("");
    fieldLocalAddressState.setText("");
    fieldLocalAddressCountry.setText("");

    }//GEN-LAST:event_btnLocalAdresSaveActionPerformed


